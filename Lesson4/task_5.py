x = int(input('Введите целое число: '))
y = int(input('Введите целое число: '))

'''
В данной программе
проверяем равенство целых чисел,
а также больше ли первое числою
Иначе отработает секция else.

'''

if x == y:
    print('Числа равны')
elif x > y:
    print('Первое число больше второго')
else:
    print('Отработала секция else')

